using CalamityMod.Buffs.DamageOverTime;
using Microsoft.Xna.Framework;
using Terraria;
using Terraria.ID;
using Terraria.ModLoader;

namespace DanksMod.Projectiles
{
	public class DryIceFire : ModProjectile
	{
		public override string Texture => "CalamityMod/Projectiles/InvisibleProj";

		public override void SetStaticDefaults()
		{
            DisplayName.SetDefault("Fire");
		}

		public override void SetDefaults()
		{
            projectile.width = 12;
            projectile.height = 12;
            projectile.friendly = true;
            projectile.ignoreWater = true;
            projectile.ranged = true;
            projectile.penetrate = 2;
            projectile.extraUpdates = 2;
            projectile.timeLeft = 50;
            projectile.usesLocalNPCImmunity = true;
            projectile.localNPCHitCooldown = 10;
            projectile.tileCollide = false;
		}

		public override void AI()
		{
			Lighting.AddLight(projectile.Center, 0.15f, 0.45f, 0f);
			if (projectile.ai[0] > 7f)
			{
				float num296 = 1f;
				if (projectile.ai[0] == 8f)
				{
					num296 = 0.25f;
				}
				else if (projectile.ai[0] == 9f)
				{
					num296 = 0.5f;
				}
				else if (projectile.ai[0] == 10f)
				{
					num296 = 0.75f;
				}
                projectile.ai[0] += 1f;
				int num297 = 230;
				if (Main.rand.NextBool(2))
				{
					for (int num298 = 0; num298 < 2; num298++)
					{
						int num299 = Dust.NewDust(new Vector2(projectile.position.X, projectile.position.Y), projectile.width, projectile.height, num297, projectile.velocity.X * 0.2f, projectile.velocity.Y * 0.2f, 100, new Color(), 0.75f);
						Dust dust = Main.dust[num299];
						if (Main.rand.NextBool(3))
						{
							dust.noGravity = true;
							dust.scale *= 1.75f;
							dust.velocity.X *= 2f;
							dust.velocity.Y *= 2f;
						}
						else
						{
							dust.noGravity = true;
							dust.scale *= 0.5f;
						}
						dust.velocity.X *= 1.2f;
						dust.velocity.Y *= 1.2f;
						dust.scale *= num296;
						dust.velocity += projectile.velocity;
					}
				}
			}
			else
			{
                projectile.ai[0] += 1f;
			}
            projectile.rotation += 0.3f * (float)projectile.direction;
		}

		public override void OnHitNPC(NPC target, int damage, float knockback, bool crit)
		{
			target.AddBuff(ModContent.BuffType<CalamityMod.Buffs.StatDebuffs.GlacialState>(), 120);
			target.AddBuff(BuffID.Frostburn, 120);
		}
	}
}
